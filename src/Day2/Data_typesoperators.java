package Day2;

public class Data_typesoperators {

	public static void main(String[] args) {

		//Types of data types -1. Primitive Data types,2.Non-Primitive data types
		//Integer Data Types
		byte a=1;
		short s=2;		
		int d=10;
		long c =3;
		
		//Floating data types
		float k=10.3f;
		double q=33.55d;
		
		//Boolean -T/F
		boolean flag=true;
		
		
		
		//String 
		String myname ="Hello World";
		
		//Operators
		System.out.println(a+k); //Addition
		System.out.println(k-a); //Subtraction
		System.out.println(a*k); //Multiplication
		System.out.println(d/c); //Division
		System.out.println(d%c); //modulus-> Gives remainder from multiplication refer divison e.g.
		
		//Print of datatypes
		
		System.out.println(a);
		System.out.println(s);
		System.out.println(d);
		System.out.println(c);
		System.out.println(k);
		System.out.println(q);
		System.out.println(myname);
		System.out.println(flag);
	}

}
